export function formatCodeName(
  adverb: string,
  adjective: string,
  noun: string,
): string {
  return `${adverb}-${adjective}-${noun}`
}

function pick<T>(list: readonly T[]): T {
  return list[Math.floor(Math.random() * list.length)]
}

export function generateCodeName(existing: Set<string>): string {
  for (let attempt = 0; attempt < 100; attempt++) {
    const name = formatCodeName(pick(ADVERBS), pick(ADJECTIVES), pick(NOUNS))
    if (!existing.has(name)) {
      return name
    }
  }
  throw new CliError(
    'Failed to generate a unique codename after 100 attempts. Please retry `naholo covert init`.',
  )
}
